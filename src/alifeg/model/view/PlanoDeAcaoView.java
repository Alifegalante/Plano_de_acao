    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getActionCommand().equals("Selecionar")) {
            this.av.selecionar();
            Object selecao = this.av.getSelecao();
            if (selecao instanceof Acao) {
                Acao av = (Acao) selecao;
//                txtEspecie.setText(Integer.toString(ev.getCodigo()));
                this.getPlanoDeAcaoSelecionada().setAcao(av);
            
            }
            this.av.dispose();
        } 
    }
