    public void selecionar() {
        if (this.tblLista.getSelectedRowCount() > 1) {
            int[] selectedRows = this.tblLista.getSelectedRows();
            this.selecao = new ArrayList<PlanoDeAcao>();
            ArrayList<PlanoDeAcao> e = (ArrayList<PlanoDeAcao>) this.selecao;
            for (int selectedRow : selectedRows) {
                e.add(listaDePlano.get(selectedRow));
            }
        } else if (this.tblLista.getSelectedRowCount() == 1) {
            this.selecao = this.listaDePlano.get(this.tblLista.getSelectedRow());
        }
    }
    
