    public void selecionar() {
        if (this.tblLista.getSelectedRowCount() > 1) {
            int[] selectedRows = this.tblLista.getSelectedRows();
            this.selecao = new ArrayList<Acao>();
            ArrayList<Acao> a = (ArrayList<Acao>) this.selecao;
            for (int selectedRow : selectedRows) {
                a.add(listaDeAcao.get(selectedRow));
            }
        } else if (this.tblLista.getSelectedRowCount() == 1) {
            this.selecao = this.listaDeAcao.get(this.tblLista.getSelectedRow());
        }
    }
    
