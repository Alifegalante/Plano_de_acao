    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getActionCommand().equalsIgnoreCase("Selecionar")) {
            this.tv.selecionar();
            Object selecao = this.tv.getSelecao();
            if (selecao instanceof Tarefa) {
                Tarefa tv = (Tarefa) selecao;
//                txtEspecie.setText(Integer.toString(ev.getCodigo()));
                this.getAcaoSelecionado().setTarefa(tv);

            }
            this.tv.dispose();
        } 
    }

